public class calculator {

	public int add(int x , int y)
	{
		return(x+y);
	}
	
	public int multi(int x , int y)
	{
		return(x*y);
	}
	
	public int subtract(int x , int y)
	{
		return(x-y);
	}
	public int divide(int x, int y)
	{
		return x/y;
	}
}
